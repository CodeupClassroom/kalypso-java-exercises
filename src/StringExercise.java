public class StringExercise {
    public static void main(String[] args) {
        String output = "We don't need no education\nWe don't need no thought control";
        System.out.println(output);

        String output2 = "Check \"this\" out!, \"s inside of \"s!"; // escaping a symbol or char
        System.out.println(output2);

        String output3 = "In windows, the main drive is usually C:\\";
        System.out.println(output3);

        String output4 = "I can do backslashes \\, double backslashes \\\\,\n and the amazing triple backslash \\\\\\!";
        System.out.println(output4);

    }
}
